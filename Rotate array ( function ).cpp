// arr - input array
// size - size of array
// d - array to be rotated by d elements

void Rotate(int arr[], int d, int n) {
   
 int temp[1000],k=0,i;
  for( i=0;i<d;i++)
  {
    temp[i]=arr[i];
  }
  for(i=d;i<n;i++)
      {
        arr[k]=arr[i];
    k++;
      }
  for(i=0;k<n;k++)
  {
    arr[k]=temp[i];
    i++;
  }
}
  
